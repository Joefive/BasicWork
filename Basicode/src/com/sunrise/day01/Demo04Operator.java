package com.sunrise.day01;
/**加、减、乘、除、MOD(实用整数，不实用小数)
*运算符如果遇到不一样类型，向上(向大的类型)转换
*/

public class Demo04Operator {
    public static void main(String[] args) {
        System.out.println(10 + 20);
        int a = 10;
        int b = 3;
        System.out.println("a-b="+(a-b));
        System.out.println("a*b="+(a*b));
        System.out.println("a/b="+(a/b));
        System.out.println("a%b="+(a%b));


    }
}
